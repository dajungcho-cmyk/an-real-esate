function getLang() {
  var stored = localStorage.getItem('an_lang')
  if (stored && I18N[stored]) return stored
  var browser = ((navigator.language || 'en')).toLowerCase().slice(0, 2)
  return I18N[browser] ? browser : 'en'
}

function setLang(lang) {
  localStorage.setItem('an_lang', lang)
  applyI18n(lang)
  try { window.dispatchEvent(new CustomEvent('an:langchange', { detail: { lang: lang } })) } catch(e) {}
}

function t(key) {
  var lang = getLang()
  return (I18N[lang] && I18N[lang][key]) || (I18N.en && I18N.en[key]) || key
}

function applyI18n(lang) {
  var dict = I18N[lang] || I18N.en
  var els = document.querySelectorAll('[data-i18n]')
  for (var i = 0; i < els.length; i++) {
    var el = els[i]
    var key = el.getAttribute('data-i18n')
    var val = (dict[key] !== undefined ? dict[key] : (I18N.en[key] !== undefined ? I18N.en[key] : key))
    var tag = el.tagName.toLowerCase()
    if (tag === 'input' || tag === 'textarea') {
      el.placeholder = val
    } else if (el.hasAttribute('data-i18n-html')) {
      el.innerHTML = val
    } else {
      el.textContent = val
      if (el.dataset.raw !== undefined) el.dataset.raw = val
    }
  }
  var valEls = document.querySelectorAll('[data-i18n-val]')
  for (var v = 0; v < valEls.length; v++) {
    var vel = valEls[v]
    var vkey = vel.getAttribute('data-i18n-val')
    var vval = (dict[vkey] !== undefined ? dict[vkey] : (I18N.en[vkey] !== undefined ? I18N.en[vkey] : ''))
    vel.value = vval
  }
  document.documentElement.lang = lang
  document.querySelectorAll('.lang-btn').forEach(function(btn) { btn.textContent = lang.toUpperCase() })
  var opts = document.querySelectorAll('.lang-opt')
  for (var j = 0; j < opts.length; j++) {
    if (opts[j].getAttribute('data-lang') === lang) {
      opts[j].classList.add('active')
    } else {
      opts[j].classList.remove('active')
    }
  }
}

function toggleLangDropdown(e) {
  if (e) e.stopPropagation()
  var switcher = e && e.target.closest('.lang-switcher')
  var d = switcher ? switcher.querySelector('.lang-dropdown') : document.querySelector('.lang-dropdown')
  if (d) {
    var isOpen = d.classList.toggle('open')
    var btn = switcher ? switcher.querySelector('.lang-btn') : (e && e.target.closest('.lang-btn')) || document.querySelector('#lang-current')
    if (btn) btn.setAttribute('aria-expanded', String(isOpen))
  }
}

function closeLangDropdown() {
  document.querySelectorAll('.lang-dropdown').forEach(function(d) { d.classList.remove('open') })
  document.querySelectorAll('.lang-btn').forEach(function(btn) { btn.setAttribute('aria-expanded', 'false') })
}

function pickLang(lang, e) {
  if (e) e.stopPropagation()
  setLang(lang)
  closeLangDropdown()
}

// Apply on load and close dropdown on outside click
document.addEventListener('DOMContentLoaded', function() {
  applyI18n(getLang())
})

document.addEventListener('click', function(e) {
  if (!e.target.closest('.lang-switcher')) {
    closeLangDropdown()
  }
})
